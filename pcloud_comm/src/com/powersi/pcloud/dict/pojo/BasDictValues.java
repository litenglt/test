	package com.powersi.pcloud.dict.pojo;
	import com.powersi.comm.bean.BaseBean;
	/**
	 * 字典key值表
	 * 存储字典key值的信息
	 * @author 黄尧
	 *
	 */
	public class BasDictValues extends BaseBean{
		private static final long serialVersionUID = 1L;
		private int id;//字典值id
		private String dict_key;//字典对应的key
		private String dict_value_code;//字典值代码
		private String dict_value_name;//字典值名称
		private int sort_id;//排序编号
		private String  remark;//购买留言
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public String getDict_key() {
			return dict_key;
		}
		public void setDict_key(String dict_key) {
			this.dict_key = dict_key;
		}
		public String getDict_value_code() {
			return dict_value_code;
		}
		public void setDict_value_code(String dict_value_code) {
			this.dict_value_code = dict_value_code;
		}
		public String getDict_value_name() {
			return dict_value_name;
		}
		public void setDict_value_name(String dict_value_name) {
			this.dict_value_name = dict_value_name;
		}
		public int getSort_id() {
			return sort_id;
		}
		public void setSort_id(int sort_id) {
			this.sort_id = sort_id;
		}
		public String getRemark() {
			return remark;
		}
		public void setRemark(String remark) {
			this.remark = remark;
		}
		public static long getSerialversionuid() {
			return serialVersionUID;
		}
		
	}


