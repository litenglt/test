package com.powersi.pcloud.dict.pojo;

import com.powersi.comm.bean.BaseBean;
/**
 * 字典表
 * 存储字典的基本信息
 * @author 黄尧
 *
 */
public class BasDict extends BaseBean{
	private static final long serialVersionUID = 1L;
	private int id;//字典id
	private String dict_key;//字典的key值
	private String key_name;//字典的名称
	private int sort_id;//排序编号
	private String editable;//是否允许用户编辑 '0'或'1'
	private String remark;//备注
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDict_key() {
		return dict_key;
	}
	public void setDict_key(String dict_key) {
		this.dict_key = dict_key;
	}
	public String getKey_name() {
		return key_name;
	}
	public void setKey_name(String key_name) {
		this.key_name = key_name;
	}
	public int getSort_id() {
		return sort_id;
	}
	public void setSort_id(int sort_id) {
		this.sort_id = sort_id;
	}
	public String getEditable() {
		return editable;
	}
	public void setEditable(String editable) {
		this.editable = editable;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	
	
}
